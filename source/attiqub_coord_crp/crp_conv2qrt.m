function [quarternion] = crp_conv2qrt(crp)

% Input: 1x3 MRP vector
% Output: 1x4 quarternion vector

qn = dot(crp,crp);
b0 = 1 / sqrt( 1 + qn );
b1 = crp(1) / sqrt( 1 + qn );
b2 = crp(2) / sqrt( 1 + qn );
b3 = crp(3) / sqrt( 1 + qn );
quarternion = [ b0 b1 b2 b3 ];

end

