function [crp] = qrt_conv2crp(quarternion)

% Input: 1x4 quarternion row vector [b0, b1, b2, b3].
% Output: 1x3 CRP vector.

c1 = quarternion(2) / quarternion(1);
c2 = quarternion(3) / quarternion(1);
c3 = quarternion(4) / quarternion(1);
crp = [c1 c2 c3];

end

